package com.sharebo.entity;

public class Timeval {
	private int sec;
	private int usec;

	public int getSec() {
		return sec;
	}

	public void setSec(int sec) {
		this.sec = sec;
	}

	public int getUsec() {
		return usec;
	}

	public void setUsec(int usec) {
		this.usec = usec;
	}

}
